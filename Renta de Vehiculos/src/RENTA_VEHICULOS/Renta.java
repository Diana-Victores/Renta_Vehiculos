    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        //BOTON BUSCAR
        try{
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/Proyecto_renta", "root", "");
            java.sql.PreparedStatement pst = cn.prepareStatement("select * from RENTA where ID = ?");
            pst.setString(1, txtid.getText().trim());

            ResultSet rs = pst.executeQuery();

            if(rs.next()){

                txtcodigo.setText(rs.getString("Codigo"));
                txtidrenta.setText(rs.getString("ID_RENTA"));
                txtserie.setText(rs.getString("SERIE"));
                txtcliente.setText(rs.getString("ID_CLIENTE"));  
                txtususario.setText(rs.getString("ID_USUARIO"));
                txttipopago.setText(rs.getString("ID_TIPO_PAGO"));
                txtfechaprestamo.setText(rs.getString("FECHA_PRESTAMO"));
                txtdevolucionfecha.setText(rs.getString("FECHA_DEVOLUCION"));
                txttotal.setText(rs.getString("TOTAL"));
                

            } else {
                JOptionPane.showMessageDialog(null, "Persona no registrada.");
            }

        }catch (Exception e){

        }

    }//GEN-LAST:event_jButton6ActionPerformed

